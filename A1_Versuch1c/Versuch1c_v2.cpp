		UniformBufferObject ubo = {};
		glm::mat4 translate = glm::translate(glm::mat4(1), glm::vec3(0));
		glm::mat4 scale = glm::scale(glm::mat4(1), glm::vec3(0.02, 0.02, 0.02));
